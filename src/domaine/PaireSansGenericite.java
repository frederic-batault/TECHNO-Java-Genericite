package domaine;

public class PaireSansGenericite {

	Object premier;
	Object second;
	
	public PaireSansGenericite(Object a, Object b) {
		super();
		this.premier = a;
		this.second = b;
	}

	public Object getPremier() {
		return premier;
	}

	public Object getSecond() {
		return second;
	}
	
	
}
